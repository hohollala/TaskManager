function getPreferredLanguage() {
  // ë¡œì»¬ ìŠ¤í† ë¦¬ì§€ í™•ì¸
  const savedLang = localStorage.getItem("preferred-language");
  if (savedLang && i18n[savedLang]) {
    return savedLang;
  }

  // ë¸Œë¼ìš°ì € ì–¸ì–´ í™•ì¸
  const browserLang = navigator.language || navigator.userLanguage;
  if (browserLang) {
    // ì „ì²´ ì–¸ì–´ ì½”ë“œ ë§¤ì¹­ ì‹œë„
    if (i18n[browserLang]) {
      return browserLang;
    }

    // ì–¸ì–´ ì½”ë“œ ì• ë‘ ê¸€ì ë§¤ì¹­ ì‹œë„ (ì˜ˆ: "ko-KR" -> "ko")
    const langPrefix = browserLang.split("-")[0];
    if (i18n[langPrefix]) {
      return langPrefix;
    }
  }

  // ê¸°ë³¸ê°’ì€ í•œêµ­ì–´
  return "ko";
}

